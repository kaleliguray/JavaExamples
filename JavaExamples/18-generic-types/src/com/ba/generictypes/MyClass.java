package com.ba.generictypes;

public class MyClass {

	private int x;
	private byte b;
	private String s;
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public byte getB() {
		return b;
	}
	
	public void setB(byte b) {
		this.b = b;
	}
	
	public String getS() {
		return s;
	}
	
	public void setS(String s) {
		this.s = s;
	}
	
}
